package KitaptanSorular;

public class Loops10 {
    public static void main(String[] args) {
        /*
        3 !le 14 arasındak! tam sayıların toplamını bulmak !ç!n kodu yazınız.
         */
        int toplam=0;
        for (int i = 3; i <15 ; i++) {
            toplam+=i;
            
        }
        System.out.println("toplam = " + toplam);
    }
}
