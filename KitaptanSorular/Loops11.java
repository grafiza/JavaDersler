package KitaptanSorular;

public class Loops11 {
    public static void main(String[] args) {
        /*
        Asagıdak! görüntüyü b!r for döngüsü kullanarak elde eden kodu yazınız.
* * * * * *
* * * * *
* * * *
* * *
* *
*
         */

        for (int i = 6; i >0 ; i--) {
            for (int j = 1; j <=i ; j++) {
                System.out.print("* ");
            }

            System.out.println();
        }
    }
}
